package com.ge.power.checklist.portal.dto;

public class ElementArributuesProp {
	
	private String attributeName;
    private String attributeId;
    private String isMendatory;
    private String defaultValue;
    
	
    public String getAttributeName() {
		return attributeName;
	}
	public void setAttributeName(String attributeName) {
		this.attributeName = attributeName;
	}
	public String getAttributeId() {
		return attributeId;
	}
	public void setAttributeId(String attributeId) {
		this.attributeId = attributeId;
	}
	public String getIsMendatory() {
		return isMendatory;
	}
	public void setIsMendatory(String isMendatory) {
		this.isMendatory = isMendatory;
	}
	public String getDefaultValue() {
		return defaultValue;
	}
	public void setDefaultValue(String defaultValue) {
		this.defaultValue = defaultValue;
	}
	

}
